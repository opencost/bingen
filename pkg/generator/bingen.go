func WriteTypeMap(out *bytes.Buffer, types []types.GenType) {
	fmt.Fprintf(out, "// Generated type map for resolving interface implementations to\n")
	fmt.Fprintf(out, "// to concrete types\n")
	fmt.Fprintf(out, "var typeMap map[string]reflect.Type = map[string]reflect.Type{\n")
	for _, t := range types {
		fmt.Fprintf(out, "    \"%s\": reflect.TypeFor[%s](),\n", t.Name(), t.Name())
	}
	fmt.Fprintf(out, "}\n\n")
}

func WriteConstants(out *bytes.Buffer, pkg string, versionSets []meta.VersionSet) {
	fmt.Fprintf(out, BingenConstants, pkg)
	fmt.Fprintf(out, "const(\n")
	for _, versionSet := range versionSets {
		versionSetName := titleName(versionSet.Name())
		constName := fmt.Sprintf("%sCodecVersion", versionSetName)
		fmt.Fprintf(out, "    // %s is used for any resources listed in the %s version set\n", constName, versionSetName)
		fmt.Fprintf(out, "    %s uint8 = %d\n\n", constName, versionSet.Version())
	}
	fmt.Fprintf(out, ")\n\n")
}

