func ReadInterfaceType(ctx GeneratorContext, t *types.InterfaceType, target vars.Target, set bool) {
	d := ctx.PushDebugRead("interface", t.Name())
	defer d.End()

	e := "="
	if set {
		e = ":="
	}

	tNameVar := ctx.NextVar()
	nameVar := ctx.NextVar()
	interfaceOKVar := ctx.NextOKVar()
	ubVar := ctx.NextVar()
	errVar := ctx.NextErrVar()

	ctx.Writeln("%s := buff.ReadString()", tNameVar)
	ctx.Writeln("_, %s, _ := resolveType(%s)", nameVar, tNameVar)
	typeCheck := ctx.PushScope("if _, ok := typeMap[%s]; !ok {", nameVar)
	ctx.WriteErrorHandler(typeCheck, fmt.Sprintf("fmt.Errorf(\"Unknown Type: %%s\", %s)", nameVar))
	typeCheck.Pop("}")

	ctx.Writeln("%s, %s := reflect.New(typeMap[%s]).Interface().(BinDecoder)", ubVar, interfaceOKVar, nameVar)
	intCheck := ctx.PushScope("if !%s {", interfaceOKVar)
	ctx.WriteErrorHandler(intCheck, fmt.Sprintf("fmt.Errorf(\"Type: %%s does not implement %%s.BinDecoder.\", %s, GeneratorPackageName)", nameVar))
	intCheck.Pop("}")

	ctx.Writeln("buff.ReadInt() // [compatibility, unused]")
	ctx.Writeln("%s := %s.UnmarshalBinaryWithContext(ctx)", errVar, ubVar)
	errCheck := ctx.PushScope("if %s != nil {", errVar)
	ctx.WriteErrorHandler(errCheck, errVar)
	errCheck.Pop("}")

	ctx.Writeln("%s %s %s.(%s)", target, e, ubVar, t.Name())
}

func ReadAliasType(ctx GeneratorContext, t *types.AliasType, target vars.Target, set bool) {
	d := ctx.PushDebugRead("alias", t.Name())
	defer d.End()

	e := "="
	if set {
		e = ":="
	}

	atype := t.Name()
	if t.IsPtr() {
		atype = "*" + t.Name()
	}

	varName := ctx.NextVar()

	ReadType(ctx, t.Alias, vars.AsTarget(varName), true)
	ctx.Writeln("%s %s %s(%s)", target, e, atype, varName)
}
