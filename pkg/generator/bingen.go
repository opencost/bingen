func ReadStructType(ctx GeneratorContext, t *types.StructType, target vars.Target, set bool) {
	d := ctx.PushDebugRead("struct", t.Name())
	defer d.End()

	e := "="
	if set {
		e = ":="
	}

	ubVar := ctx.NextVar()
	errVar := ctx.NextErrVar()

	ctx.Writeln("%s := &%s{}", ubVar, t.Name())
	ctx.Writeln("buff.ReadInt() // [compatibility, unused]")
	ctx.Writeln("%s := %s.UnmarshalBinaryWithContext(ctx)", errVar, ubVar)
	errCheck := ctx.PushScope("if %s != nil {", errVar)
	ctx.WriteErrorHandler(errCheck, errVar)
	errCheck.Pop("}")

	if t.IsPtr() {
		ctx.Writeln("%s %s %s", target, e, ubVar)
	} else {
		ctx.Writeln("%s %s *%s", target, e, ubVar)
	}
}

