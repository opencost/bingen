func Generate(dir string, pkg string, bufferImport string, tc types.TypeCollection) {
	var out bytes.Buffer

	targetTypes := tc.Types()

	// Sort Types to ensure Deterministic Ordering
	sort.Slice(targetTypes, func(i, j int) bool {
		return targetTypes[i].Name() < targetTypes[j].Name()
	})

	streamableTypes := GetStreamableTypes(targetTypes)

	ctxFactory := NewGeneratorContextFactory(&out, 4, ErrorHandler)

	WriteHeader(&out, pkg)
	WriteImports(&out, pkg, bufferImport, tc.Imports())
	WriteConstants(&out, pkg, tc.VersionSets())
	WriteDivider(&out, "Configuration")
	WriteConfiguration(&out)
	WriteDivider(&out, "Type Map")
	WriteTypeMap(&out, targetTypes)
	WriteDivider(&out, "Type Helpers")
	WriteTypeHelpers(&out)
	WriteDivider(&out, "Stream Helpers")
	WriteStreamableTypeMap(&out, streamableTypes)
	WriteStreamSupportTypes(&out)
	WriteDivider(&out, "String Table Writer")
	WriteStringTableWriter(&out)
	WriteDivider(&out, "String Table Reader")
	WriteStringTableReader(&out)
	WriteDivider(&out, "Codec Context")
	WriteCodecContext(&out)
	WriteDivider(&out, "Binary Codec")
	WriteBinaryCodecInterfaces(&out)

	for _, t := range targetTypes {
		if st, ok := t.(*types.StructType); ok {
			WriteDivider(&out, t.Name())
			WriteMarshaller(ctxFactory.NewContext(st.Opts), t)
			WriteUnmarshaller(ctxFactory.NewContext(st.Opts), t)
			if st.Opts.IsStreamable {
				WriteDivider(&out, fmt.Sprintf("%sStream", t.Name()))
				WriteStreamReader(ctxFactory.NewContext(st.Opts).WithErrorHandler(StreamErrorHandler), t)
			}
		}
	}

	result, err := format.Source(out.Bytes())
	if err != nil {
		fmt.Println("Failed to format:", err)
		return
	}

	err = os.WriteFile(fmt.Sprintf("%s/%s_codecs.go", dir, pkg), result, 0600)
	if err != nil {
		return
	}
}

func WriteHeader(out *bytes.Buffer, pkg string) {
	fmt.Fprintf(out, "////////////////////////////////////////////////////////////////////////////////\n")
	fmt.Fprintf(out, "//\n")
	fmt.Fprintf(out, "//                             DO NOT MODIFY\n//\n")
	fmt.Fprintf(out, "//                          ┻━┻ ︵ヽ(`Д´)ﾉ︵ ┻━┻\n//\n")
	fmt.Fprintf(out, "//\n")
	fmt.Fprintf(out, "//            This source file was automatically generated by bingen.\n")
	fmt.Fprintf(out, "//\n")
	fmt.Fprintf(out, "////////////////////////////////////////////////////////////////////////////////\n\n")
	fmt.Fprintf(out, "package %s\n\n", pkg)
}

