	fmt.Fprintf(out, "import (\n")
	//fmt.Fprintf(out, "    \"encoding\"\n")
	fmt.Fprintf(out, "    \"sync\"\n")
	fmt.Fprintf(out, "    \"fmt\"\n")
	fmt.Fprintf(out, "    \"io\"\n")
